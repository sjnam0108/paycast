package kr.co.paycast.viewmodels.pay;

public class BasicItem {
	
	private int id;
	private String name;
	
	public BasicItem(int id, String name) {
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
