package kr.co.paycast.viewmodels.self;

public class ResMenuCategory {

	private String storeId;
	private String storeName;
	private String storeImage;
	private String menuType;
	private String storeBackground;
	private String categoryNum;
	private String unit;
	public String getStoreId() {
		return storeId;
	}
	public void setStoreId(String storeId) {
		this.storeId = storeId;
	}
	public String getStoreName() {
		return storeName;
	}
	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}
	public String getStoreImage() {
		return storeImage;
	}
	public void setStoreImage(String storeImage) {
		this.storeImage = storeImage;
	}
	public String getMenuType() {
		return menuType;
	}
	public void setMenuType(String menuType) {
		this.menuType = menuType;
	}
	public String getStoreBackground() {
		return storeBackground;
	}
	public void setStoreBackground(String storeBackground) {
		this.storeBackground = storeBackground;
	}
	public String getCategoryNum() {
		return categoryNum;
	}
	public void setCategoryNum(String categoryNum) {
		this.categoryNum = categoryNum;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = "KRW";
	}
	
	
	
}
